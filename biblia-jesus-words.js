/**
 * Verifica si un versiculo contiene palabras de Jesus
 * @param {string} libro - ID del libro (ej: 'mateo', 'juan')
 * @param {number} capitulo - Numero de capitulo
 * @param {number} versiculo - Numero de versiculo
 * @returns {boolean} true si el versiculo contiene palabras de Jesus
 */
function isJesusWords(libro, capitulo, versiculo) {
    var key = libro + '_' + capitulo;
    if (JESUS_WORDS[key]) {
        return JESUS_WORDS[key].indexOf(versiculo) !== -1;
    }
    return false;
}

/**
 * Obtiene todos los versiculos de Jesus en un capitulo
 * @param {string} libro - ID del libro
 * @param {number} capitulo - Numero de capitulo
 * @returns {number[]} Array de numeros de versiculos
 */
function getJesusVersesInChapter(libro, capitulo) {
    var key = libro + '_' + capitulo;
    return JESUS_WORDS[key] || [];
}

/**
 * Cuenta el total de versiculos marcados como palabras de Jesus
 * @returns {number} Total de versiculos
 */
function countJesusVerses() {
    var total = 0;
    for (var key in JESUS_WORDS) {
        if (JESUS_WORDS.hasOwnProperty(key)) {
            total += JESUS_WORDS[key].length;
        }
    }
    return total;
}

/**
 * Obtiene estadisticas por libro
 * @returns {Object} Objeto con conteo por libro
 */
function getJesusWordsStats() {
    var stats = {};
    for (var key in JESUS_WORDS) {
        if (JESUS_WORDS.hasOwnProperty(key)) {
            var libro = key.split('_')[0];
            if (!stats[libro]) {
                stats[libro] = { chapters: 0, verses: 0 };
            }
            stats[libro].chapters++;
            stats[libro].verses += JESUS_WORDS[key].length;
        }
    }
    return stats;
}

